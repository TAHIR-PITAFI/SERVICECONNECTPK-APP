// ─── Helper Functions ─────────────────────────────────────────────────────────

/**
 * Get provider distance to a given sector
 */
export function getDistanceToSector(provider, sector) {
  if (!sector) return provider.distanceMap.default || 10;
  return provider.distanceMap[sector] || provider.distanceMap.default || 10;
}

/**
 * Get all providers for a given service type
 */
export function getProvidersByService(serviceType) {
  return PROVIDERS.filter(p => p.services.includes(serviceType));
}

/**
 * Get all providers near a sector (within maxKm)
 */
export function getProvidersNearSector(serviceType, sector, maxKm = 15) {
  return PROVIDERS.filter(p => {
    if (!p.services.includes(serviceType)) return false;
    const dist = getDistanceToSector(p, sector);
    return dist <= maxKm;
  });
}
